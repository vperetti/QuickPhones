    @Action
    /**
     * Abre tela de pesquisa
     */
    public void Pesquisar(){
        if (pesquisa == null) {
            pesquisa = new FormSearch();
            Set<FormSearchColumn> colunas = new LinkedHashSet<FormSearchColumn>();



            FormSearchColumn fscNome = new FormSearchColumn();
            fscNome.setProperty("nome");
            fscNome.setPropertyClass(String.class);
            fscNome.setLabel("Nome");
            fscNome.setWidth(300);
            fscNome.setAlign(SwingConstants.LEFT);
            fscNome.setTypeSearch(DAOConstants.LIKE);
            colunas.add(fscNome);

            FormSearchColumn fscTelefone = new FormSearchColumn();
            fscTelefone.setProperty("telefone");
            fscTelefone.setPropertyClass(String.class);
            fscTelefone.setLabel("Telefone");
            fscTelefone.setWidth(200);
            fscTelefone.setAlign(SwingConstants.LEFT);
            fscTelefone.setTypeSearch(DAOConstants.LIKE);
            colunas.add(fscTelefone);

            FormSearchColumn fscEndereco = new FormSearchColumn();
            fscEndereco.setProperty("endereco");
            fscEndereco.setPropertyClass(String.class);
            fscEndereco.setLabel("Endereço");
            fscEndereco.setWidth(200);
            fscEndereco.setAlign(SwingConstants.LEFT);
            fscEndereco.setTypeSearch(DAOConstants.LIKE);
            colunas.add(fscEndereco);
            
            FormSearchColumn fscContato = new FormSearchColumn();
            fscContato.setProperty("contato");
            fscContato.setPropertyClass(String.class);
            fscContato.setLabel("Contato");
            fscContato.setWidth(200);
            fscContato.setAlign(SwingConstants.LEFT);
            fscContato.setTypeSearch(DAOConstants.LIKE);
            colunas.add(fscContato);

            

            FormSearchColumn fscId = new FormSearchColumn();
            fscId.setProperty("id");
            fscId.setPropertyClass(Long.class);
            fscId.setLabel("ID");
            fscId.setWidth(100);
            fscId.setAlign(SwingConstants.RIGHT);
            fscId.setTypeSearch(DAOConstants.ABOVE_EQUAL);
            colunas.add(fscId);

            pesquisa.setIdProperty("id");
            pesquisa.setIdClass(Long.class);
            pesquisa.setColumns(colunas);
            pesquisa.setDefaultProperty("nome");
            pesquisa.setEntityClass(Contato.class);
            pesquisa.setDaoClass(ContatoDAO.class);
            pesquisa.setDaoFactory((DAOFactoryAbstract) getDaoFactory());
            pesquisa.setTitle("Pesquisa de contatos.");
            pesquisa.makeForm();
            pesquisa.getRbAny().setSelected(true);
            //pesquisa.setKeySearch("teste");
            //pesquisa.doSearch();
        }
        
        pesquisa.openSearch();
        Contato vem = pesquisa.getEntity();
        if (vem != null){
            txtId.setText(String.valueOf(vem.getId()));
            Alterar();
        }
    }

